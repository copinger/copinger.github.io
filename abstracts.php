    <a name="Oka"><b>Takashi Oka (ISSP, U-Tokyo)</b></a><br>
    <i>The Schwinger effect in solid-state physics</i>
    <p style="text-indent: 30px">
      The Schwinger effect, i.e., tunnel creation of particle pairs in electric fields, is one of the oldest theoretical predictions in non-perturbative quantum field theory. I will explain how this effect provides the key to understand several laser-field induced nonequilibrium phenomena in solid-state systems.
    </p>
    <p>
    (1) Application in strongly correlated electron systems:<br>
    In Mott insulators, charge excitations are frozen due to Coulomb repulsion. Strong dc and ac fields can create charge excitations, i.e., doublon-hole pairs, and melt the insulating state. I will explain this process using the Bethe ansatz approach with comparison to numerical results [1].
    </p>
    <p>
    (2) Application in Dirac semimetals and geometric effects:<br>
    In weakly interacting Dirac semimetals, the non-adiabatic tunneling can be understood by a Landau-Zener like models. One interesting aspect is how geometric effects (Berry phase etc) appear in the dynamics leading to processes that break chiral symmetry [2].
  </p>
  <p>
    (3) Floquet approach to Dirac semimetals in laser fields:<br>
    Floquet theory is a simple and powerful approach to study quantum systems driven by time-periodic perturbations [3]. I will explain its application to Dirac semimetals in laser fields.
  </p>
  <p>
    (4') Application in holographic QCD:<br>
    In holographic QCD, it is possible to study non-perturbative quantum dynamics using a classical field theory in a curved geometry. (Without explaining the detail,) I will present some results [4,5] compared with the above topics (1)-(3) to provide further research motivations.
  </p>
