    <i>Observation of the Breit-Wheeler Process in Heavy-Ion Collisions</i>
    <p style="text-indent: 30px">
      Ultra-relativistic heavy ion collisions are expected to produce some of the strongest magnetic fields (10<sup>13</sup>-10<sup>16</sup> Tesla) in the Universe [1]. Recently, there has been increased interest in the magnetic fields produced by heavy ion collisions and their possible observational impacts through emergent magnetohydrodynamical phenomena in Quantum Chromodynamics, like the Chiral Magnetic Effect [2]. The initial strong electromagnetic fields produced in heavy ion collisions have been proposed as a source of linearly-polarized, quasi-real photons [3] that can interact via the Breit-Wheeler process to produce e<sup>+</sup> e<sup>-</sup> pairs [4].
    </p>
    <p style="text-indent: 30px">
      In this talk I present STAR measurements of e<sup>+</sup> e<sup>-</sup> pair production in ultra-peripheral and peripheral Au+Au collisions at &#8730;s<sub>NN</sub> = 200 GeV. A comprehensive study of the pair kinematics is presented to distinguish the &gamma;&gamma; &#8594; e<sup>+</sup> e<sup>-</sup> process from other possible production mechanisms. Furthermore,  the measured distribution of e<sup>+</sup> e<sup>-</sup> pairs reveals a striking fourth-order angular modulation which demonstrates that the colliding photons are linearly polarized. The observed azimuthal asymmetry has implications for vacuum birefringence [5], a phenomenon predicted in 1936 in which empty space can split light according to its polarization components when subjected to a strong magnetic field.  Furthermore, these measurements provide the first direct experimental evidence that ultra-relativistic heavy ion collisions are capable of producing the strongest magnetic fields in the known Universe over an extended spatial distribution.
