    <i>QED in intense background fields: Foundation and modern applications</i>
    <p style="text-indent: 30px">
  Quantum electrodynamics (QED) is a well established physical theory and its predictions have been confirmed experimentally in various regimes and with extremely high accuracy. However, there are still areas of QED that deserve theoretical and experimental investigation, especially when physical processes occur in the presence of intense background electromagnetic fields, i.e., of the order of the so-called "critical" field of QED. In the presence of electromagnetic fields of such a high strength the vacuum behaves as a birefringent and unstable medium, whose properties depend on the background field.<br>
