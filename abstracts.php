    <a name="Takasan"><b>Kazuaki Takasan (UC, Berkeley)</b></a><br>
    <i>Dielectric breakdown of strongly correlated insulators in one dimension</i>
    <p style="text-indent: 30px">
      Application of sufficiently strong electric fields to insulators induces finite currents and then the insulators become metallic. This phenomenon is called dielectric breakdown and known as a fundamental nonequilibrium and nonlinear transport phenomenon in solids. For band insulators (i.e., free fermion systems), it is called Zener breakdown and well-understood [1]. In contrast, the dielectric breakdown of strongly correlated insulators is less understood and has been actively studied even in recent years both experimentally [2] and theoretically [3]. However, most of the previous studies are limited to specific examples, mainly fermionic Mott insulators. While dielectric breakdowns of other strongly correlated insulators, such as charge density-wave insulators, bosonic Mott insulators, and Kondo insulators, have been also experimentally relevant, universal properties common in the breakdown phenomena have not been well-understood.
    </p>
    <p style="text-indent: 30px">
      In this talk, I would like to present our recent results about the universal aspects in the dielectric breakdown of one-dimensional strongly correlated insulators [4]. Combining bosonization techniques with a theory of quantum tunneling (Dykhne-Davis-Pechukas formula), we develop an effective field-theoretical description of dielectric breakdown with a non-Hermitian sine-Gordon theory. Then, we derive an analytic formula of the threshold field which is a many-body generalization of the Landau-Zener formula. Importantly, we point out that the threshold field contains a previously overlooked factor originating from charges of elementary excitations, which should be significant when a system has fractionalized excitations. We apply our results to integrable lattice models and confirm that our formula is valid in a broad range including the weak coupling regime, indicating its wide and potential applicability.
    </p>
    <p style="text-indent: 30px">
      The talk will start from the brief review of the studies of dielectric breakdown in solids and then move to our recent results. If time allows, I will also address some future issues and open problems in this field.
    </p>
